print('My name is')
for i in range(0,10): #this goes between 0-9
    print('Jimmy ' + str(i))

#Make i change by two each time
print('My name is')
for i in range(0,10,2): #the third thing in this changes how much it changes by each time.
    print('Jimmy ' + str(i))

#Make it go down by one each time
print('My name is')
for i in range(5,-1,-1): #This makes it change by -1 each time. It starts at five and ends at zero.
    print('Jimmy ' + str(i))